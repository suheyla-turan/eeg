class LocalVideoEntry {
  const LocalVideoEntry({
    required this.id,
    required this.title,
    required this.assetPath,
    this.description = '',
    this.category = '',
    this.durationSeconds = 0,
  });

  final String id;
  final String title;
  final String assetPath;
  final String description;
  final String category;
  final int durationSeconds;

  VideoContent toVideoContent() {
    return VideoContent(
      videoId: id,
      title: title,
      description: description,
      category: category,
      // storageUrl alanı asset yolu olarak kullanılır.
      storageUrl: assetPath,
      duration: durationSeconds,
      active: true,
      createdAt: DateTime(2026, 1, 1),
    );
  }
}
